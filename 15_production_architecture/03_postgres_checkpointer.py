section("4. Time-travel: inspect or replay from past state")
# ═══════════════════════════════════════════════════════════════════════════════
print('''
  config = {"configurable": {"thread_id": "conv-user42-20260601"}}

  # List all checkpoints for a thread (most recent first)
  async for checkpoint in app_graph.aget_state_history(config):
      print(f"  checkpoint_id={checkpoint.config['configurable']['checkpoint_id']}")
      print(f"  node_name    ={checkpoint.metadata.get('source', '?')}")
      print(f"  message_count={len(checkpoint.values.get('messages', []))}")
      print()

  # Replay from a specific past checkpoint
  past_config = {
      "configurable": {
          "thread_id":     "conv-user42-20260601",
          "checkpoint_id": "uuid-of-past-checkpoint",
      }
  }
  past_state = await app_graph.aget_state(past_config)
  print("State at that point:", past_state.values)

  # Branch: continue from a past point with a different message
  branch_result = await app_graph.ainvoke(
      {"messages": [HumanMessage(content="Actually, what about PROJ-102 instead?")]},
      config=past_config,
  )
  # Creates a new branch in the checkpoint tree; original history preserved
''')


