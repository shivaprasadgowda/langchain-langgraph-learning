section("6. Local development with SQLite (no PostgreSQL needed)")
# ═══════════════════════════════════════════════════════════════════════════════
print('''
  For local development without a PostgreSQL instance:

  from langgraph.checkpoint.sqlite.aio import AsyncSqliteSaver

  async with AsyncSqliteSaver.from_conn_string("checkpoints.db") as checkpointer:
      await checkpointer.setup()
      app_graph = build_and_compile_graph(checkpointer)

      result = await app_graph.ainvoke(
          {"messages": [HumanMessage(content="Hello")]},
          config={"configurable": {"thread_id": "local-test"}},
      )

  # checkpoints.db is a local file — persistent across restarts
  # Switch to AsyncPostgresSaver in staging/production

  # .env.local:
  #   DATABASE_TYPE=sqlite
  #   DATABASE_URL=checkpoints.db

  # .env.production:
  #   DATABASE_TYPE=postgres
  #   POSTGRES_URL=postgresql+psycopg://user:pass@db:5432/app
''')


