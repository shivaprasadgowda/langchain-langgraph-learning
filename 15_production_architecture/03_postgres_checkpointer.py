section("5. Checkpoint lifecycle for a HITL conversation")
# ═══════════════════════════════════════════════════════════════════════════════
print("""
  Thread "conv-T1" checkpoints written over time:

  checkpoint_id | node             | messages | notes
  ──────────────┼──────────────────┼──────────┼───────────────────────────
  chk-001       | __start__        | 1        | HumanMessage saved
  chk-002       | llm_node         | 2        | AIMessage with tool_calls
  ── GRAPH PAUSED (interrupt_before=["tools"]) ──────────────────────────
  ... user sees approval dialog, waits 10 minutes ...
  ── User approves, POST /chat/approve ─────────────────────────────────
  chk-003       | tools            | 4        | 2 ToolMessages added
  chk-004       | llm_node (final) | 5        | Final AIMessage

  At any point between chk-002 and chk-003, a server restart is safe.
  On restart, AsyncPostgresSaver reads from PostgreSQL; no state is lost.
""")


