# ═══════════════════════════════════════════════════════════════════════════════
print('''
  # Install pgvector extension once:
  #   CREATE EXTENSION IF NOT EXISTS vector;

  # Create embeddings table:
  #   CREATE TABLE documents (
  #       id        SERIAL PRIMARY KEY,
  #       content   TEXT,
  #       embedding vector(1536),   -- text-embedding-3-small dimension
  #       metadata  JSONB
  #   );
  #   CREATE INDEX ON documents USING ivfflat (embedding vector_cosine_ops);

  from langchain_community.vectorstores import PGVector
  from langchain_openai import OpenAIEmbeddings

  vectorstore = PGVector(
      collection_name = "devops_docs",
      connection_string = os.environ["POSTGRES_URL"],
      embedding_function = OpenAIEmbeddings(model="text-embedding-3-small"),
  )

  # Add documents
  vectorstore.add_documents(docs)

  # Search
  results = vectorstore.similarity_search("Kubernetes pod restarts", k=4)

  # One Postgres instance handles:
  #   - LangGraph checkpoints (chat state, HITL pauses, time-travel)
  #   - pgvector embeddings  (RAG retrieval)
  #   - User data            (accounts, preferences)
  # Fewer moving parts = simpler infra, easier backups, one connection pool.
''')
