"""
Concept: PostgreSQL checkpointer for durable LangGraph state.

In production, MemorySaver is NOT acceptable:
  - Data lost on server restart
  - Not shared across multiple FastAPI instances
  - Cannot support time-travel or compliance requirements

AsyncPostgresSaver writes a checkpoint row after every graph node:
  table: checkpoints
    thread_id    TEXT     ← identifies the conversation
    checkpoint_id UUID    ← unique per node execution
    parent_id    UUID     ← links to previous checkpoint (forms a tree)
    checkpoint   JSONB    ← serialised state (messages, context, etc.)
    metadata     JSONB    ← node name, run_id, etc.
    created_at   TIMESTAMPTZ

This enables:
  - Durable multi-turn conversations across restarts
  - Horizontal scaling (any server can resume any thread)
  - Time-travel: replay from any past checkpoint
  - HITL: graph pauses, state is safe until the user approves

Run:
    python 15_production_architecture/03_postgres_checkpointer.py
"""


def section(title: str) -> None:
    print(f"\n{'='*70}")
    print(f"  {title}")
    print('='*70)


# ═══════════════════════════════════════════════════════════════════════════════
section("1. Database schema (created by checkpointer.setup())")
# ═══════════════════════════════════════════════════════════════════════════════
print("""
  AsyncPostgresSaver.setup() creates these tables automatically:

  CREATE TABLE IF NOT EXISTS checkpoints (
      thread_id      TEXT        NOT NULL,
      checkpoint_ns  TEXT        NOT NULL DEFAULT '',
      checkpoint_id  UUID        NOT NULL,
      parent_id      UUID,
      type           TEXT,
      checkpoint     JSONB       NOT NULL,
      metadata       JSONB       NOT NULL DEFAULT '{}',
      PRIMARY KEY (thread_id, checkpoint_ns, checkpoint_id)
  );

  CREATE TABLE IF NOT EXISTS checkpoint_writes (
      thread_id      TEXT        NOT NULL,
      checkpoint_ns  TEXT        NOT NULL DEFAULT '',
      checkpoint_id  UUID        NOT NULL,
      task_id        UUID        NOT NULL,
      idx            INTEGER     NOT NULL,
      channel        TEXT        NOT NULL,
      type           TEXT,
      value          JSONB,
      PRIMARY KEY (thread_id, checkpoint_ns, checkpoint_id, task_id, idx)
  );

  checkpoint_writes holds in-progress node outputs before they are
  committed as a full checkpoint — this is the durability mechanism.
""")


# ═══════════════════════════════════════════════════════════════════════════════
section("2. Setting up the checkpointer in FastAPI")
# ═══════════════════════════════════════════════════════════════════════════════
print('''
  import os
  from contextlib import asynccontextmanager
  from fastapi import FastAPI
  from psycopg_pool import AsyncConnectionPool
  from langgraph.checkpoint.postgres.aio import AsyncPostgresSaver

  pool: AsyncConnectionPool | None = None

  @asynccontextmanager
  async def lifespan(api: FastAPI):
      global pool

      # POSTGRES_URL format:
      #   postgresql+psycopg://user:password@host:5432/dbname
      pool = AsyncConnectionPool(
          conninfo=os.environ["POSTGRES_URL"],
          min_size=2,     # always keep 2 warm connections
          max_size=10,    # burst up to 10 under load
          open=False,
      )
      await pool.open()

      checkpointer = AsyncPostgresSaver(pool)
      await checkpointer.setup()  # idempotent — safe to call on every start

      # Compile the graph with the persistent checkpointer
      app_graph = build_and_compile_graph(checkpointer)

      yield  # FastAPI serves requests here

      await pool.close()

  api = FastAPI(lifespan=lifespan)
''')


# ═══════════════════════════════════════════════════════════════════════════════
section("3. Using the checkpointer in a request")
# ═══════════════════════════════════════════════════════════════════════════════
print('''
  # thread_id is generated by the client (browser) once per conversation.
  # Pass it in the LangGraph config on every request.

  config = {"configurable": {"thread_id": "conv-user42-20260601"}}

  # Turn 1: graph starts fresh, writes first checkpoint after each node
  result = await app_graph.ainvoke(
      {"messages": [HumanMessage(content="Search Jira for login bugs")]},
      config=config,
  )
  print(result["messages"][-1].content)
  # "I found 3 Jira tickets for login bugs: PROJ-101, PROJ-102, PROJ-103."

  # Turn 2: graph reads the checkpoint, sees previous messages, continues
  result = await app_graph.ainvoke(
      {"messages": [HumanMessage(content="Create a ticket for the first one")]},
      config=config,
  )
  # The graph remembers PROJ-101 from the previous turn
  print(result["messages"][-1].content)
  # "I\'ve created a follow-up ticket PROJ-201 linked to PROJ-101."
''')


